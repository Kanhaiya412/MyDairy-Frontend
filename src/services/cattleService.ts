import apiClient from "./apiClient";

/* ============================================================
   ENUMS (MUST MATCH BACKEND EXACTLY)
============================================================ */

export type CattleCategory = "COW" | "BUFFALO";

export type CattleBreed =
  | "JERSY"
  | "SAHIVAL"
  | "GIR"
  | "DEVLI"
  | "INDIAN"
  | "OTHER_COW"
  | "MURRAH"
  | "JAFRAWADI"
  | "MEHSANA"
  | "BADHAWARI"
  | "BANNI"
  | "OTHER_BUFFALO";

export type CattleGender = "MALE" | "FEMALE";

export type CattleStatus = "ACTIVE" | "SOLD" | "DEAD" | "ARCHIVED";

/* ============================================================
   UPDATED CattleEntry MODEL (SYNCED WITH BACKEND)
============================================================ */

export interface CattleEntry {
  id?: number;                    // Auto generated by backend
  userId: number;
  
  cattleId: string;               // Unique cattle code
  cattleName: string;
  cattleCategory: CattleCategory;
  cattleBreed: CattleBreed;

  gender?: CattleGender;          // NEW ✔

  cattlePurchaseDate: string;
  cattleDay: string;
  cattlePurchaseFrom: string;
  cattlePurchasePrice: number;

  cattleSoldDate?: string;
  cattleSoldTo?: string;
  cattleSoldPrice?: number;

  totalCattle: number;

  imageUrl?: string | null;       // NEW ✔

  status?: CattleStatus;          // ACTIVE | SOLD | DEAD | ARCHIVED

  createdAt?: string;             // NEW ✔
  updatedAt?: string;             // NEW ✔
}

/* ============================================================
   ADD CATTLE ENTRY
============================================================ */

export const addCattleEntry = async (entry: CattleEntry) => {
  try {
    console.log("[cattleService] Adding cattle entry:", entry);

    const response = await apiClient.post("/cattle/add", entry);

    console.log("✅ Cattle added:", response.data);
    return response.data;

  } catch (error: any) {
    console.error(
      "❌ Failed to add cattle entry:",
      error.response?.data || error.message
    );
    throw error;
  }
};

